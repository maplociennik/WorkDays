package workdays;

public class ReportDays {

	private long numberOfDays;
	private String numberOfWeeks;
	private long numberOfWorkingDays;
	private long numberOfHollidays;
	
	public long getNumberOfDays() {
		return numberOfDays;
	}
	public void setNumberOfDays(long numberOfDays) {
		this.numberOfDays = numberOfDays;
	}
	public String getNumberOfWeeks() {
		return numberOfWeeks;
	}
	public void setNumberOfWeeks(String numberOfWeeks) {
		this.numberOfWeeks = numberOfWeeks;
	}
	public long getNumberOfWorkingDays() {
		return numberOfWorkingDays;
	}
	public void setNumberOfWorkingDays(long numberOfWorkingDays) {
		this.numberOfWorkingDays = numberOfWorkingDays;
	}
	public long getNumberOfHollidays() {
		return numberOfHollidays;
	}
	public void setNumberOfHollidays(long numberOfHollidays) {
		this.numberOfHollidays = numberOfHollidays;
	}
	
	
}
